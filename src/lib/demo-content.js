export function toDateValue(value) {
  if (!value) {
    return null
  }

  if (typeof value?.toDate === 'function') {
    return value.toDate()
  }

  if (value instanceof Date) {
    return value
  }

  const parsed = new Date(value)
  return Number.isNaN(parsed.getTime()) ? null : parsed
}

export function formatDisplayDate(value, fallback = 'Oct 24, 2024') {
  const date = toDateValue(value)
  if (!date) {
    return fallback
  }

  return new Intl.DateTimeFormat('en-US', {
    month: 'short',
    day: 'numeric',
    year: 'numeric',
  }).format(date)
}

export function formatRelativeUpdate(value, fallback = 'Updated recently') {
  const date = toDateValue(value)
  if (!date) {
    return fallback
  }

  const days = Math.max(
    1,
    Math.round((Date.now() - date.getTime()) / (1000 * 60 * 60 * 24))
  )

  if (days === 1) {
    return 'Updated 1 day ago'
  }

  return `Updated ${days} days ago`
}

export function getDisplayName(email, fallback = 'Curator') {
  if (!email) {
    return fallback
  }

  const localPart = email.split('@')[0] || fallback
  return localPart
    .split(/[._-]+/)
    .filter(Boolean)
    .map((segment) => segment[0].toUpperCase() + segment.slice(1))
    .join(' ')
}

export function getInitials(value, fallback = 'ER') {
  if (!value) {
    return fallback
  }

  const parts = String(value)
    .trim()
    .split(/\s+/)
    .filter(Boolean)

  if (parts.length === 0) {
    return fallback
  }

  return parts
    .slice(0, 2)
    .map((part) => part[0].toUpperCase())
    .join('')
}

export function normalizeSearchValue(value) {
  return String(value || '').trim().toLowerCase()
}

export function getRoleLabel(role) {
  if (role === 'admin') {
    return 'Admin'
  }

  if (role === 'faculty') {
    return 'Faculty'
  }

  return 'Student'
}

export function getSubjectTone(subject) {
  const value = normalizeSearchValue(subject)

  if (value.includes('math')) {
    return 'primary'
  }

  if (value.includes('computer') || value.includes('cs')) {
    return 'secondary'
  }

  if (value.includes('physics') || value.includes('engineering')) {
    return 'secondary'
  }

  if (value.includes('electronics')) {
    return 'secondary'
  }

  return 'neutral'
}
